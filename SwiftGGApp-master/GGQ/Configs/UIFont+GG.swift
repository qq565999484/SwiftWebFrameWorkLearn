import UIKit

extension UIFont {

    class func gg_h1_Font() -> UIFont {
        return UIFont.systemFontOfSize(21, weight: UIFontWeightLight)
    }

    class func gg_h2_Font() -> UIFont {
        return UIFont.systemFontOfSize(17, weight: UIFontWeightLight)
    }

    class func gg_h3_Font() -> UIFont {
        return UIFont.systemFontOfSize(14, weight: UIFontWeightLight)
    }

    class func gg_p_Font() -> UIFont {
        return UIFont.systemFontOfSize(12, weight: UIFontWeightLight)
    }

}
